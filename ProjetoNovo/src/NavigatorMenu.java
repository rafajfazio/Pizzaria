    private void ConfirmarEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConfirmarEditarMouseClicked
        // TODO add your handling code here:
        if (EditClienteTable.getSelectedRow() > -1) {
            String clienteEditInputNome = ClienteEditInputNome.getText();
            String clienteEditInputCpf = ClienteEditInputCpf.getText();
            String clienteEditInputTelefone = ClienteEditInputTelefone.getText();
            String clienteEditInputEndereco = ClienteEditInputEndereco.getText();
            
            if (clienteEditInputNome.isEmpty() || clienteEditInputTelefone.isEmpty() || clienteEditInputEndereco.isEmpty() || clienteEditInputCpf.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
                
            } else {
                
                int idEdit = ((int) EditClienteTable.getValueAt(EditClienteTable.getSelectedRow(), 0));
                int countCpf = 0;
                
                for (Cliente i: ListaClientes) {                      
                    if (i.getCpf().contains(clienteEditInputCpf)) {
                        countCpf++;
                    }
                }
                
                if (countCpf <= 1) {
                    for (Cliente i: ListaClientes) {                      
                        if (i.getId() == idEdit) {
                            Cliente cliente_novo = new Cliente(idEdit, clienteEditInputEndereco, clienteEditInputTelefone, clienteEditInputNome, clienteEditInputCpf);
                            int indexCliente = ListaClientes.indexOf(i);
                            ListaClientes.set(indexCliente, cliente_novo);
                        } 
                    } 
                } else {
                    JOptionPane.showMessageDialog(null, "Este CPF esta presente em outro cliente");
                }

                ClienteEditInputNome.setText("");
                ClienteEditInputCpf.setText("");
                ClienteEditInputTelefone.setText("");
                ClienteEditInputEndereco.setText("");
            }
        }
    }//GEN-LAST:event_ConfirmarEditarMouseClicked

