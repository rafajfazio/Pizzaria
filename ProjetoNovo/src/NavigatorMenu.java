    private void jButton16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton16MouseClicked
        // TODO add your handling code here:
        // campo nome
        String inputFuncionarioNome = InputFuncionarioNome.getText();
        String inputFuncionarioCpf = InputFuncionarioCpf.getText();
        String inputFuncionarioSalario = InputFuncionarioSalario.getText();
        String inputFuncionarioBonus = InputFuncionarioBonus.getText();
        String inputFuncionarioPlaca = InputFuncionarioPlaca.getText();
        
        if (inputFuncionarioCpf.isEmpty() || inputFuncionarioNome.isEmpty() || inputFuncionarioSalario.isEmpty() || inputFuncionarioPlaca.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Prencha todos os campos");
        } else {
            
            boolean entregadorOk = true;
            
            for (Entregador i: ListaEntregador) {
                if (i.getCpf().contains(inputFuncionarioCpf)) {
                    entregadorOk = false;
                }
            }
            
            if (entregadorOk) {
                if (inputFuncionarioBonus.isEmpty()) {
                    try {
                        float salario = Float.parseFloat(inputFuncionarioSalario);
                        Entregador entregador = new Entregador(inputFuncionarioPlaca, idFuncionario, salario, inputFuncionarioNome, inputFuncionarioCpf);
                        ListaEntregador.add(entregador);
                    } catch(NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Campo de valores esta incorreto");
                    }
                } else {
                    try {
                        float bonus = Float.parseFloat(inputFuncionarioBonus);
                        float salario = Float.parseFloat(inputFuncionarioSalario);
                        Entregador entregador = new Entregador(inputFuncionarioPlaca, idFuncionario, salario, inputFuncionarioNome, inputFuncionarioCpf);
                        float salarioTotal = entregador.getCalc(salario, bonus);
                        entregador.setSalario(salarioTotal);
                        ListaEntregador.add(entregador);
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "O campo bonus esta incorreto");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ja existi um entregador com esse CPF!");
            }
        }
        idFuncionario++;
        
        DefaultTableModel tabelaDeFuncionarios = (DefaultTableModel) TabelaDeFuncionarios.getModel();
        tabelaDeFuncionarios.setRowCount(0);
        for (Entregador i: ListaEntregador) {
            Object[] dados = {i.getId(), i.getNome(), i.getCpf(), i.getSalario(), i.getPlacaMoto()};
            tabelaDeFuncionarios.addRow(dados);
        }
    }//GEN-LAST:event_jButton16MouseClicked

